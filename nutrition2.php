        <h2>6. โภชนาการกับพัฒนาการทางสมอง</h2>
        <p>สารอาหารบางชนิด เช่น DHA และโอเมก้า-3 มีบทบาทสำคัญในการพัฒนาสมองของเด็ก โดยเฉพาะในช่วงวัยที่สมองกำลังเจริญเติบโตอย่างรวดเร็ว การได้รับสารอาหารเหล่านี้จากอาหาร เช่น ปลาทะเลน้ำลึก ถั่ว และเมล็ดพืช จะช่วยเสริมสร้างความจำและการเรียนรู้ นอกจากนี้ วิตามินและแร่ธาตุ เช่น วิตามินดี วิตามินบี และไอโอดีน ยังมีความสำคัญต่อการทำงานของสมอง การให้เด็กได้รับสารอาหารที่สมดุลจึงเป็นสิ่งจำเป็นสำหรับการพัฒนาสมองที่ดี</p>
