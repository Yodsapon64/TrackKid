        <h2>4. การป้องกันโรคด้วยโภชนาการ</h2>
        <p>โภชนาการที่ดีช่วยป้องกันโรคที่เกิดจากการขาดสารอาหาร เช่น โรคโลหิตจางจากการขาดธาตุเหล็ก และโรคขาดวิตามินต่างๆ นอกจากนี้ ยังช่วยป้องกันโรคอ้วน โรคเบาหวานในเด็ก และโรคหัวใจในอนาคต การสอนให้เด็กมีพฤติกรรมการกินที่ดีและมีความรู้เกี่ยวกับโภชนาการตั้งแต่เล็กเป็นวิธีที่มีประสิทธิภาพในการลดความเสี่ยงของการเกิดโรคเรื้อรัง การจัดการโภชนาการที่เหมาะสมในช่วงวัยเด็กยังช่วยส่งเสริมการเจริญเติบโตและพัฒนาการที่สมบูรณ์ และเสริมสร้างระบบภูมิคุ้มกันที่แข็งแรง</p>
