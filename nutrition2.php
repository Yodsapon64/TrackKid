        <h2>3. การเลือกอาหารที่เหมาะสม</h2>
        <p>อาหารที่เหมาะสมจะช่วยให้เด็กได้รับสารอาหารครบถ้วน ซึ่งจะช่วยในการสร้างเซลล์และเนื้อเยื่อ การเจริญเติบโตของกระดูกและกล้ามเนื้อ การทำงานของสมอง และระบบภูมิคุ้มกัน การเลือกอาหารที่เหมาะสมควรเน้นไปที่การมีผัก ผลไม้ เนื้อสัตว์ที่ไม่มีไขมันสูง ธัญพืชเต็มเมล็ด และผลิตภัณฑ์นมที่มีไขมันต่ำ ควรหลีกเลี่ยงอาหารที่มีน้ำตาลและเกลือสูง รวมถึงอาหารที่ผ่านกระบวนการผลิตมากเกินไป เช่น ขนมขบเคี้ยวและน้ำอัดลม การให้เด็กมีส่วนร่วมในการเลือกอาหารและการทำอาหารยังเป็นวิธีที่ดีในการสร้างนิสัยการกินที่ดี</p>
