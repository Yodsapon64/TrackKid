        <h2>2. อาหารที่สำคัญในแต่ละช่วงวัย</h2>
        <p>เด็กในแต่ละช่วงวัยมีความต้องการสารอาหารที่แตกต่างกัน เด็กอ่อนอาจต้องการน้ำนมแม่หรือสูตรนมที่เหมาะสม ขณะที่เด็กวัยหัดเดินควรได้รับอาหารที่มีความหลากหลาย เช่น ผัก ผลไม้ โปรตีนจากเนื้อสัตว์ และไขมันดี เพื่อช่วยในการเจริญเติบโตและพัฒนาการทางสมอง ส่วนเด็กวัยเรียนควรได้รับอาหารที่เสริมสร้างพลังงานและสารอาหารที่เพียงพอต่อการเรียนรู้และการทำกิจกรรมต่างๆ เช่น การออกกำลังกาย นอกจากนี้ ควรมีการแบ่งสัดส่วนอาหารที่เหมาะสมในแต่ละมื้อ และลดการบริโภคน้ำตาลและไขมันที่ไม่ดีต่อสุขภาพ</p>
